    private void btnOpenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOpenActionPerformed
        JFileChooser jfc = new JFileChooser();
        jfc.setCurrentDirectory(new File(System.getProperty("user.home")));
        FileNameExtensionFilter filter = new FileNameExtensionFilter("jpg", "png", "gif");
        int open = jfc.showOpenDialog(this);
        if (open == JFileChooser.APPROVE_OPTION) {
            
//            pilih file yang akan di pake
            File file = jfc.getSelectedFile();
            
//            buat image menjadi imageIcon
            ImageIcon img = new ImageIcon(file.getAbsolutePath());
            
            // ambil lebar dan panjang dari gambar yang dipilih
            Rectangle rect = Icon.getBounds();
            java.awt.Image scaledimage = img.getImage().getScaledInstance(rect.width, rect.height, java.awt.Image.SCALE_DEFAULT);
            // atur skala gambar agar agar sesuai dengan label
            
            // konversi gambar ke ImageIcon agar bisa ditampilkan
            img = new ImageIcon(scaledimage);
            Icon.setIcon(img);
            String nama = file.getName();
            FileName.setText(nama);
        }
    }//GEN-LAST:event_btnOpenActionPerformed

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        Rectangle rec = Icon.getBounds();
        BufferedImage bufImg = new BufferedImage(rec.width, rec.height, BufferedImage.TYPE_INT_ARGB);
        Icon.paint(bufImg.getGraphics());
        JFileChooser chooser = new JFileChooser();
        chooser.setFileSelectionMode(JFileChooser.FILES_ONLY);
        int save = chooser.showSaveDialog(this);
        if (save == JFileChooser.APPROVE_OPTION) {
            File f = chooser.getSelectedFile();
            try {
                ImageIO.write(bufImg, "jpg", f);
                JOptionPane.showMessageDialog(this, "Gambar anda Berhasil Disimpan", "Success", JOptionPane.INFORMATION_MESSAGE);

            } catch (IOException ex) {
                JOptionPane.showMessageDialog(this, "Error!!!", "ERROR", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnSaveActionPerformed

